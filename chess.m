%% Chess Board
clc;
clear all;
close all
n = 8;
m = 20;
black = zeros(n,n);
white = zeros(n,n);
white(:,:) = 255;
c = zeros(n*m, n*m);
for i = 1:2:n
    for j = 1:n
        for k = 1:m
            c(i*k, j) = 255;
        end
    end
end
figure(1)
imshow(black)
figure(2)
imshow(white)
figure(3)
imshow(c)